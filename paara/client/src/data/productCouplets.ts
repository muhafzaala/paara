function hash(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = ((h * 31) + s.charCodeAt(i)) >>> 0;
  return h;
}

export function getCouplet(category: string, seed: string): string {
  const pool = COUPLETS[category] ?? FALLBACK;
  return pool[hash(seed) % pool.length];
}
